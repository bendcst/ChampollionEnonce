package champollion;

public class ServicePrevu {

    private int volCM;
    private int volTD;
    private int volTP;

    public ServicePrevu(int volCM, int volTD, int volTP) {
        this.volCM = volCM;
        this.volTD = volTD;
        this.volTP = volTP;
    }

    public int getVolCM() {
        return volCM;
    }

    public int getVolTD() {
        return volTD;
    }

    public int getVolTP() {
        return volTP;
    }

    public void setVolCM(int volCM) {
        this.volCM = volCM;
    }

    public void setVolTD(int volTD) {
        this.volTD = volTD;
    }

    public void setVolTP(int volTP) {
        this.volTP = volTP;
    }
    
    
}
